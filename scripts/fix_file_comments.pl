#!/usr/bin/perl


use File::Find;
use File::Basename;

find(
    {
        wanted => \&findfiles,
    },
    'include'
);


find(
    {
        wanted => \&findfiles,
    },
    'tests'
);



sub findfiles
{

    $header = <<EOH;
/*
 * sst-basic-blocks - an open source library of core audio utilities
 * built by Surge Synth Team.
